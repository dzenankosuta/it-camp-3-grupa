// STRINGOVI //
// Stringovi su immutable vrednosti. 
// Stringovi su primitivnog (osnovnog) tipa podataka.

// Za dobijanje duzine stringa koristimo length metodu:
let a = "string"
duzina = a.length 

// String mozemo zapisati na nekoliko nacina:

// 1.
// Koristeci duple navodnike:
"Danas je bilo promenljivo vreme."
// 2.
// Koristeci obicne navodnike:
'Danas je bilo promenljivo vreme.'
// 3. Template Literals (Back-Tics sintaksa)
`Danas je bilo promenljivo vreme.`

// Dupli navodnici:
"Danas je bilo 'promenljivo' vreme."

"Danas je bilo \"promenljivo\" vreme."

// Obicni navodnici:
'Danas je bilo "promenljivo" vreme.'

'Danas je bilo \'promenljivo\' vreme.'
